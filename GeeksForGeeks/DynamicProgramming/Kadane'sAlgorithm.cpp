class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        // Your code here
        long long sum=0;
        long long res=-1000000000;
        for(int x=0; x<n; x++)
        {
            sum=max((long long)arr[x]+sum,(long long)arr[x]);
            res=max(res,sum);
        }
        return res;
        
    }
};

